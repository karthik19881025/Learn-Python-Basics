"""

Four ways, we can represent int data type value

1. Decimal form (base 10) -> 0 to 9 -> NO PREFIX Needed
2. Binary form (base 2) -> 0 and 1 -> PREFIX 0b or 0B
3. Octal form (base 8) -> 0 to 7 -> PREFIX 0o or 0O
4. Hexa Decimal form (base 16) -> 0 to 9 and A to F -> PREFIX 0x or 0X

"""
a = 123
print(a)

a = 0b1111
print(a)

a = 0o127
print(a)

a = 0x127
print(a)